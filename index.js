// تنظيف النص: حذف علامات الترقيم والتشكيل وتوحيد الهمزات
function normalizeText(text) {
  return text
    .trim()
    .replace(/[؟?.!،,;:\-_"']/g, '')
    .replace(/[\u064B-\u0652]/g, '')   // حذف التشكيل
    .replace(/[أإآ]/g, 'ا')            // توحيد الهمزات
    .replace(/ى/g, 'ي')
    .replace(/\s+/g, ' ')
    .trim();
}

