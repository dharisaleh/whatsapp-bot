// المؤشّر يختفي تلقائياً عند إرسال أول رد أو بعد ~25 ثانية.
async function markAsReadWithTyping(messageId) {
  if (!messageId) return;
  try {
    await axios.post(
      `https://graph.facebook.com/v18.0/${PHONE_NUMBER_ID}/messages`,
      {
        messaging_product: 'whatsapp',
        status: 'read',
        message_id: messageId,
        typing_indicator: { type: 'text' }
      },
      { headers: { Authorization: `Bearer ${WHATSAPP_TOKEN}`, 'Content-Type': 'application/json' } }
    );
  } catch (error) {
    console.error('markAsRead error:', error.response?.data || error.message);
  }
}

// يكشف إن كانت رسالة المستخدم تقييماً (👍/👎) لآخر جواب.
// يُشترط أن تكون قصيرة (إيموجي فقط) حتى لا نخلط سؤالاً يحوي إيموجي بالتقييم.
function parseFeedbackEmoji(text) {
  const t = text.trim();
  if ([...t].length > 3) return null; // ليست إيموجي مفرد
  if (t.includes('👍')) return 'up';
  if (t.includes('👎')) return 'down';
  return null;
}

