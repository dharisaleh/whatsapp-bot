// يحدد أي مصدر/مصادر لها علاقة بالسؤال، ويرجع نصها فقط بدل المكتبة كاملة.
// فيه شبكة أمان: لو ما تأكدنا، يرجع المكتبة كاملة (نفس الوضع القديم الآمن).

const ROUTER_INSTRUCTIONS = `أنت موجّه ذكي لبوت قانوني. مهمتك تحديد المصادر المتعلقة بسؤال المستخدم من القائمة التالية فقط:

${sectionsIndex}

القواعد:
- أرجع معرفات (id) المصادر المتعلقة فقط، مفصولة بفاصلة. مثال: law,regulation_165
- كن كريماً: لو الموضوع موجود بالقانون وله تفصيل باللائحة، أرجع الاثنين معاً.
- لو ما قدرت تحدد أي مصدر بثقة، أو السؤال عام/غامض/حساب، أرجع كلمة: ALL
- ممنوع تكتب أي شي ثاني — معرفات أو ALL فقط، بدون شرح.`;

async function selectRelevantContent(from, text) {
  // طلبات المتابعة القصيرة → أعد استخدام آخر مصادر اختيرت (بدل المكتبة كاملة)
  // تشمل: "شرح" / "تفاصيل" + الردود الرقمية لاختيار مادة من قائمة (١، 2، ٣...)
  const cleaned = text.trim().replace(/[؟?.!،,]/g, '');
  const isFollowUp = cleaned === 'شرح' || cleaned === 'تفاصيل';
  // رد رقمي قصير: أرقام عربية أو إنجليزية فقط، وأقل من 4 خانات (اختيار من قائمة)
  const isNumericChoice = /^[\d١٢٣٤٥٦٧٨٩٠]{1,3}$/.test(cleaned);

  if ((isFollowUp || isNumericChoice) && lastSectionIds[from]?.length) {
    return buildContent(lastSectionIds[from], false);
  }

  try {
    const routing = await anthropic.messages.create({
      model: 'claude-sonnet-4-6',
      max_tokens: 60,
      system: ROUTER_INSTRUCTIONS,
      messages: [{ role: 'user', content: text }]
    });

    const raw = (routing.content[0]?.text || '').trim();

    // شبكة الأمان: لو قال ALL أو ما رجّع شي → المكتبة كاملة
    if (!raw || raw.toUpperCase().includes('ALL')) {
      return { content: allLawText, ids: VALID_SECTION_IDS, usedFallback: true };
    }

    const ids = raw
      .split(',')
      .map(s => s.trim())
      .filter(id => VALID_SECTION_IDS.includes(id));

    // لو ما طلع ولا معرّف صحيح → المكتبة كاملة
    if (ids.length === 0) {
      return { content: allLawText, ids: VALID_SECTION_IDS, usedFallback: true };
    }

    lastSectionIds[from] = ids;
    return buildContent(ids, false);
  } catch (error) {
    // أي خطأ بالموجّه → المكتبة كاملة (لا نكسر البوت أبداً)
    console.error('Router error:', error.message);
    return { content: allLawText, ids: VALID_SECTION_IDS, usedFallback: true };
  }
}

function buildContent(ids, usedFallback) {
  let content = '';
  for (const id of ids) {
    if (sectionsContent[id]) {
      content += `\n\n========== ${sectionTitleById[id]} ==========\n\n${sectionsContent[id]}`;
    }
  }
  if (!content) {
    return { content: allLawText, ids: VALID_SECTION_IDS, usedFallback: true };
  }
  return { content, ids, usedFallback };
}

